

  //   The most common types are:
  //   if: Executes code if a condition is true.
  //   if...else: Executes one block of code if a condition is true, and another if false.
  //   else if: Allows testing multiple conditions in sequence.
  //   switch: Matches an expression with multiple values and executes the corresponding code.

    let day = 3;
    switch (day) {
        case 1:
            console.log("Monday");
            break;
        case 2:
            console.log("Tuesday");
            break;
        case 3:
            console.log("Wednesday");
            break;
        case 4:
            console.log("Thursday");
            break;
        default:
            console.log("Invalid day");
}