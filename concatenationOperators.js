

// 1. Using the + Operator: The + operator is the primary way to concatenate strings in JavaScript.  
//    When used with strings, it joins them into a single string.

let firstName = "John";
let lastName = "Ramu";
let fullName = firstName + " " + lastName;  // Concatenate strings with a space
console.log(fullName);  // Output: "John Ramu"

// 2. Using the += Operator: The += operator can also be used to append one string to another. 
//                           This is shorthand for concatenating and re-assigning the result.


let greeting = "Hello";
greeting += " World!";  // Concatenate " World!" to the original string
console.log(greeting);   // Output: "Hello World!"
