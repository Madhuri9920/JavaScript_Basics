//  Summary of Type Conversion Methods:
//  Conversion	Method	Example
//  To String	String(value)	String(123) → "123"
//  To Number	Number(value)	Number("123") → 123
//  To Integer	parseInt(value)	parseInt("123px") → 123
//  To Float	parseFloat(value)	parseFloat("12.34abc") → 12.34
//  To Boolean	Boolean(value)	Boolean(0) → false